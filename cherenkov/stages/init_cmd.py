def run_init(profile: str | None = None, force: bool = False) -> int:
    """Execute `cherenkov init`.

    Returns exit code (0 = success).
    """
    print("=" * 60)
    print("  CHERENKOV init -- zero-config setup")
    print("=" * 60)

    # 1. Check if cherenkov.toml already exists
    toml_path = Path.cwd() / "cherenkov.toml"
    if toml_path.exists() and not force:
        print(f"\n  cherenkov.toml already exists at {toml_path}")
        print("  Use --force to overwrite or edit it manually.\n")
        return 0

    # 2. Detect Ollama
    ollama_ok, ollama_detail = detect_ollama()
    print(f"\n  [1/4] Ollama:     {'OK' if ollama_ok else 'NO'} {ollama_detail}")

    # 3. Detect device
    device = detect_device()
    print(f"  [2/4] Device:     {device}")

    # 4. Detect spec files
    spec_files = detect_spec_files()
    if spec_files:
        print(f"  [3/4] Spec files: {len(spec_files)} found")
        for s in spec_files:
            print(f"         - {s}")
    else:
        print("  [3/4] Spec files: none found (add paths manually to cherenkov.toml)")

    # 5. Determine profile
    resolved_profile = profile or "laptop"
    print(f"  [4/4] Profile:    {resolved_profile}")

    # 6. Write cherenkov.toml
    toml_content = generate_toml(spec_files, resolved_profile, device)
    try:
        toml_path.write_text(toml_content, encoding="utf-8")
        print(f"\n  [OK] Generated {toml_path}")
    except OSError as e:
        print(f"\n  [FAIL] Failed to write {toml_path}: {e}")
        return 1

    # 7. Summary
    print()
    print("-" * 60)
    print("  Next steps:")
    print("    Run:    ./bin/cherenkov doctor    # verify your setup")
    if spec_files:
        print("    Run:    ./bin/cherenkov validate --target <url>  # run tests")
    else:
        print(f"    Edit:   {toml_path} -- add your OpenAPI spec path under [sources]")
        print("    Then:   ./bin/cherenkov doctor")
    print()
    print("  Defaults: offline, free, deterministic")
    print("  Upgrade:  edit profile in cherenkov.toml, or set CHERENKOV_* env vars")
    print("=" * 60)

    return 0

def detect_ollama() -> tuple[bool, str]:
    """Check if Ollama is installed and reachable.

    Returns (available, detail_string).
    """
    # Check if ollama binary exists
    ollama_path = shutil.which("ollama")
    if not ollama_path:
        return False, "Ollama binary not found on PATH. Install from https://ollama.com"

    # Check if daemon is running
    try:
        result = subprocess.run(
            [ollama_path, "list"],
            capture_output=True,
            text=True,
            timeout=10,
        )
        if result.returncode != 0:
            return False, f"Ollama daemon not running ({result.stderr.strip()})"
    except (subprocess.TimeoutExpired, FileNotFoundError):
        return False, "Ollama daemon unreachable (timeout)"

    return True, "Ollama daemon is running"


def detect_device() -> str:
    """Detect GPU vs CPU runtime.

    Returns one of: "GPU", "CPU", "UNKNOWN"
    """
    device = Config.detect_ollama_device()
    if device in ("GPU", "CPU"):
        return device
    return "UNKNOWN"


def detect_spec_files() -> list[str]:
    """Autodetect OpenAPI spec files."""
    return LayeredConfig().autodetect_spec()



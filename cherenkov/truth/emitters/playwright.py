"""
cherenkov/truth/emitters/playwright.py — E4-1: Playwright emitter.

Produces a Playwright test suite from the Truth Model endpoints. Reuses the Track A
GENERATE/REVIEW/EJECT pattern.
"""

from __future__ import annotations

from pathlib import Path
from typing import Any

from cherenkov.core.contracts import DivergenceReport
from cherenkov.core.truth_model import TruthModel
from cherenkov.truth.emitters.interface import Emitter


class PlaywrightEmitter(Emitter):
    """Emits a Playwright test suite from Truth Model endpoints."""

    def emit(
        self,
        truth_model: TruthModel,
        output_path: Path,
        divergences: list[DivergenceReport] | None = None,
        **kwargs: Any,
    ) -> Path:
        output_path.mkdir(parents=True, exist_ok=True)

        endpoints = truth_model.get_endpoints()
        test_file = output_path / "generated_api_tests.spec.ts"

        lines = [
            'import { test, expect } from "@playwright/test";',
            "",
            "// Auto-generated by CHERENKOV PlaywrightEmitter",
            "// Source: Truth Model",
            "",
        ]

        for ep in endpoints:
            label = ep.label.replace("{", "").replace("}", "")
            method = label.split(" ")[0].lower() if " " in label else "get"
            path_part = label.split(" ")[-1] if " " in label else label
            safe_name = (
                path_part.replace("/", "_")
                .replace("-", "_")
                .replace("{", "")
                .replace("}", "")
            )

            lines.append(f'test.describe("{label}", () => {{')
            lines.append(
                f'  test("GET {label} returns 200", async ({{ request }}) => {{'
            )
            lines.append(f'    const resp = await request.{method}("{path_part}");')
            lines.append("    expect(resp.status()).toBe(200);")
            lines.append("  });")
            lines.append("});")
            lines.append("")

        test_file.write_text("\n".join(lines), encoding="utf-8")
        return test_file
